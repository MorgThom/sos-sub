using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;

namespace RoboSub.Exceptions
{
    /// <summary>
    /// Overheated
    /// </summary>
    [Serializable]
    class OverheatException : InternalConditionException
    {
        public OverheatException()
        {
        }

        public OverheatException(string message)
            : base(message)
        {
        }

        public OverheatException(string message, Exception innerException)
            : base(message, innerException)
        {
        }

        protected OverheatException(SerializationInfo info, StreamingContext context)
            : base(info, context)
        {
        }
    }
    /// <summary>
    /// Humidity too high.
